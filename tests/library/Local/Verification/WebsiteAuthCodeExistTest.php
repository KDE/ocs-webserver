class Local_Verification_WebsiteAuthCodeExistTest extends PHPUnit_Framework_TestCase
{

    const SALT_KEY = 'MakeItAndPlingIt';

    protected function setUp()
    {
        require_once APPLICATION_LIB . '/Local/Application.php';

        // Create application, bootstrap, and run
        $this->bootstrap = new Local_Application(
            APPLICATION_ENV,
            Zend_Registry::get('configuration'),
            Zend_Registry::get('cache')
        );

        parent::setUp(); // TODO: Change the autogenerated stub
    }

    protected function tearDown()
    {
        parent::tearDown(); // TODO: Change the autogenerated stub
    }

    public function testAuthCodeChecking()
    {
        $url = 'http://www.dschinnweb.de';
        $websiteValidation = new Local_Verification_WebsiteProject();
        $verificationResult = $websiteValidation->testForAuthCodeExist($url, $this->generateAuthCode($url));
        $this->assertEquals(false, $verificationResult, 'AuthCode was not found.');
    }

    private function generateAuthCode($url)
    {
        if (empty($url)) {
            return null;
        }
        return md5($url . self::SALT_KEY);
    }
}
